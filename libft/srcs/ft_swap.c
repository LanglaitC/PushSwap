#include "../inc/libft.h"

char	*ft_swap(char *s)
{
	int		i;
	int		j;
	char	c;

	j = ft_strlen(s) - 1;
	i = 0;
	while (i < j)
	{
		c = s[i];
		s[i] = s[j];
		s[j] = c;
		i++;
		j--;
	}
	return (s);
}
